// video memory begins here
#define VGA_ADDRESS 0xB8000

// all 16 VGA colors
#define BLACK          0x0
#define BLUE           0x1
#define GREEN          0x2
#define CYAN           0x3
#define RED            0x4
#define MAGENTA        0x5
#define BROWN          0x6
#define GRAY           0x7
#define DARK_GRAY      0x8
#define BRIGHT_BLUE    0x9
#define BRIGHT_GREEN   0xA
#define BRIGHT_CYAN    0xB
#define BRIGHT_RED     0xC
#define BRIGHT_MAGENTA 0xD
#define YELLOW         0xE
#define WHITE          0xF
